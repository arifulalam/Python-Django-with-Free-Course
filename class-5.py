# While Loop

#Print BANGLADESH 10 times
i = 1
while (i <= 10):
    print('BANGLADESH')
    i += 1

# Print even and odd number
j = 1
while j <= 10:
    if(j % 2 == 0): print('{} is even number.'.format(j))
    else: print('{} is odd number.'.format(j))
    j += 1


# Print all even numbers
k = 0
while k <= 20:
    print(k)
    k += 2

