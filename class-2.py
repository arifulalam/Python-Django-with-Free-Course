# variables

a = 10
b = 17
c = 9

# sumation of two
sum = a + b
print("Sum of {} and {} is: ".format(a, b), sum)

# substraction of two numbers
subs = a - b
print("Substraction of {} and {} is: ".format(a, b), subs)

# multiplication of two numbers
multiply = a * b
print("Multiplication of {} and {} is: ".format(a, b), multiply)

# division of two numbers
div = a / b
print("Division of {} and {} is: ".format(a, b), div)

# sumation of three
sum = a + b + c
print("Sum of {}, {} and {} is: ".format(a, b, c), sum)

# area of a triangle
base = 10
height = 23
area = 0.5 * base * height
print("area of triangle having {} base, {} height is".format(base, height), area)

# area of a quadrilateral
width = 10
length = 23
area = width * length
print("area of quadrilateral having {} width, {} length is".format(width, length), area)

# area of a Circle
diameter = 23
radius = diameter / 2
area = (22/7) * pow(radius, 2)
print("area of circle having {} diameter, {} radius is".format(diameter, radius), area)